package com.sky.controller.admin;

import com.sky.dto.DishDTO;
import com.sky.dto.DishPageQueryDTO;
import com.sky.entity.Dish;
import com.sky.result.PageResult;
import com.sky.result.Result;
import com.sky.service.DishService;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.*;

/**
 * 菜品管理
 */
@RestController
@RequestMapping("/admin/dish")
@Slf4j
public class DishController {

    @Autowired
    DishService dishService;
    /**
     * 菜品分页查询
     * @param dishPageQueryDTO
     * @return
     */
    @GetMapping("/page")
    public Result<PageResult<DishDTO>> dishList(DishPageQueryDTO dishPageQueryDTO){
        log.info("菜品分页查询");
        PageResult<DishDTO> pageResult = dishService.dishList(dishPageQueryDTO);

        return Result.success(pageResult);
    }

    /**
     * 菜品起售停售
     * @param status
     * @param id
     * @return
     */
    @PostMapping("/status/{status}")
    public Result startOrStop(@PathVariable Integer status, Long id){
        log.info("菜品起售停售");

        dishService.startOrStop(status,id);
        return Result.success();
    }

    /**
     * 根据id查询菜品和对应的口味数据
     * @param ids
     * @return
     */
    @GetMapping("/{id}")
    public Result<DishDTO> getByIdWithFlavor(@PathVariable Long id){
        log.info("根据id查询菜品和对应的口味数据...");
        DishDTO dishDTO = dishService.getByIdWithFlavor(id);
        return Result.success(dishDTO);
    }

    /**
     * 新增菜品
     * @param dishDTO
     * @return
     */
    @PostMapping
    public Result<String> save(@RequestBody DishDTO dishDTO){
        log.info("新增菜品: {}", dishDTO.getName());
        dishService.save(dishDTO);
        return Result.success();
    }

    /**
     * 修改菜品
     * @param dishDTO
     * @return
     */
    @PutMapping
    public Result<String> update(@RequestBody DishDTO dishDTO){
        log.info("修改菜品: {}", dishDTO.getName());
        dishService.update(dishDTO);
        return Result.success();
    }


}
